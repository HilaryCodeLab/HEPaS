-- Create the HEPaS database
CREATE DATABASE HEPaS;
USE HEPaS;

    CREATE TABLE courses 
(
	course_code VARCHAR(3) PRIMARY KEY NOT NULL,
	course_title VARCHAR(50) NOT NULL,
	year_start_offer VARCHAR(4) NOT NULL,
    year_end_offer VARCHAR(4),
	course_coordinator_name VARCHAR(50),
	coordinator_email VARCHAR(100) NULL,
	coordinator_mobile_no VARCHAR(10)
);

CREATE TABLE students 
(
    student_id VARCHAR(8) PRIMARY KEY NOT NULL,
    first_name VARCHAR(50) NOT NULL,
    last_name VARCHAR(50) NOT NULL,
    oust_email VARCHAR(100) NOT NULL,
    mobile_no VARCHAR(10) NOT NULL,
    course_code VARCHAR(3) NOT NULL,
    units_attempted INT NOT NULL,
    units_completed INT NOT NULL,
    course_status ENUM ('Completed', 'In Progress'),
    FOREIGN KEY (course_code) REFERENCES courses(course_code)
);

   CREATE TABLE unit_scores 
(
		record_No INT PRIMARY KEY,
    	student_id  VARCHAR(8) NOT NULL,
    	unit_code VARCHAR(512),
    	unit_title VARCHAR(512),
		score FLOAT NOT NULL,
    	result_grade VARCHAR(2),
		FOREIGN KEY (student_id) REFERENCES students(student_id)
);
